// Definimos las dimensiones del elipsoide
let a = 5; // Radio en el eje x
let b = 7; // Radio en el eje y
let c = 10; // Radio en el eje z

// Calculamos el volumen del elipsoide
let volumen = (4/3) * Math.PI * a * b * c;

// Mostramos el resultado en la consola
console.log("El volumen del elipsoide es: " + volumen);
