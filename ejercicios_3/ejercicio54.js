// Definimos las variables
let primerNumero = 10;
let segundoNumero = 5;
let resultado;

// Verificamos si el primer número es mayor que el segundo
if (primerNumero > segundoNumero) {
    // Restamos el segundo número al primero
    resultado = primerNumero - segundoNumero;
    console.log("El resultado de la resta es: " + resultado);
} else {
    // Si el primer número no es mayor que el segundo, mostramos un mensaje
    console.log("La operación no es posible de realizar.");
}
